public class Apto {
    public String name;
    public Integer number;
    public Integer floor;
    public Boolean availabe;
    
    public Apto() {
    }

    public Apto(String name, Integer number, Integer floor, Boolean availabe) {
        this.name = name;
        this.number = number;
        this.floor = floor;
        this.availabe = availabe;
    }

}
